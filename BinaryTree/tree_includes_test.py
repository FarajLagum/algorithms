from BinaryTreeNodeClass import Node
from tree_includes import tree_includes_depth_first_iterative


def test_00():
    a = Node("a")
    b = Node("b")
    c = Node("c")
    d = Node("d")
    e = Node("e")
    f = Node("f")

    a.left = b
    a.right = c
    b.left = d
    b.right = e
    c.right = f

    #      a
    #    /   \
    #   b     c
    #  / \     \
    # d   e     f

    assert tree_includes_depth_first_iterative(a, "e") == True


